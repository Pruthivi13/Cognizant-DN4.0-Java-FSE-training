CREATE TABLE AuditLog (
    LogID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    AccountID NUMBER,
    LogDate DATE,
    Message VARCHAR2(255)
);


CREATE OR REPLACE TRIGGER LogTransaction
AFTER INSERT ON Transactions
FOR EACH ROW
BEGIN
    INSERT INTO AuditLog (LogID, TransactionID, Action, ActionTime)
    VALUES (AuditLog_seq.NEXTVAL, :NEW.TransactionID, 'INSERT', SYSDATE);
END;
/

CREATE SEQUENCE AuditLog_seq START WITH 1 INCREMENT BY 1;